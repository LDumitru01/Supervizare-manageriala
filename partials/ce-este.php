            <li>
                <span class="font-semibold text-accent">Reflecția asupra practicii</span>
                – înțelegi ce se întâmplă cu tine, echipa, deciziile.
            </li>
            <li>
                <span class="font-semibold text-accent">Crearea de soluții</span>
